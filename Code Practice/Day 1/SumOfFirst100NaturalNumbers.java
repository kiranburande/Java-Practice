package PracticeJava;

public class SumOfFirst100NaturalNumbers 
{
	public static void main(String[] args) 
	{
		int sum=0;
		
		for(int x=1;x<=100;x++)
		{
			sum=sum+x;
		}
		
		System.out.println(sum);
	}

}
 

